def pick_storyteller_prompt(child_age: int) -> tuple[str, str]:
    """Возвращает (system_prompt_template, rotation_hint_template) для возраста.

    Граница: ≤4 лет — toddler-промпт (8 архитектур, короче, проще),
            ≥5 лет — основной промпт (25 архитектур, сложнее, глубже).
    """
    if child_age <= 4:
        return SYSTEM_STORYTELLER_TODDLER, ROTATION_HINT_TODDLER_TEMPLATE
    return SYSTEM_STORYTELLER, ROTATION_HINT_TEMPLATE


# Регулярки для парсинга первой строки ответа модели.
# Полный формат (5-6 лет, v2): «Группа В, архитектура 14 — сказка-вопрос;
#                                регистр 1 — бюрократический абсурд»
# Полный формат (5-6 лет, v1): «Группа В, архитектура 11 — перевёртыш»
#                              (без регистра — обратная совместимость)
# Toddler-формат (3-4 лет):     «Архитектура: 3 — возвращение» (группы нет)
import re as _re
# Полный формат: захватываем группу (А/Б/В/Г), архитектуру (число),
# опционально регистр юмора (число после «; регистр»). Если регистра нет
# (старый формат) — m.group(3) == None.
STORY_MARKER_FULL_RE = _re.compile(
    r"^\s*Группа\s+([А-Г])\s*,\s*архитектура\s+(\d+)"
    r"(?:[^;\n]*;\s*регистр\s+(\d+))?"
    r"[^\n]*\n?",
    _re.IGNORECASE,
)
STORY_MARKER_TODDLER_RE = _re.compile(
    r"^\s*Архитектура\s*:?\s*(\d+)"
    r"(?:[^;\n]*;\s*регистр\s+(\d+))?"
    r"[^\n]*\n?",
    _re.IGNORECASE,
)


def parse_story_marker(
    text: str,
) -> tuple[str, str | None, int | None, int | None]:
    """Извлекает группу, архитектуру и регистр юмора из первой строки ответа
    модели, отрезает эту строку от текста.

    Поддерживает четыре формата:
    - «Группа В, архитектура 14 — сказка-вопрос; регистр 1 — ...» (kids v2)
      → group='В', arch=14, register=1
    - «Группа В, архитектура 11 — перевёртыш» (kids v1, без регистра)
      → group='В', arch=11, register=None
    - «Архитектура 3 — возвращение; регистр 1 — игра со словом» (toddler v2)
      → group=None, arch=3, register=1
    - «Архитектура: 3 — возвращение» (toddler v1, без регистра)
      → group=None, arch=3, register=None

    Возвращает (cleaned_text, group_letter, architecture_number, humor_register).
    Если маркер не найден — (text, None, None, None).
    """
    # Сначала пробуем полный формат (со словом «Группа» в начале).
    m = STORY_MARKER_FULL_RE.match(text)
    if m:
        group = m.group(1).upper()
        arch = int(m.group(2))
        register = int(m.group(3)) if m.group(3) else None
        cleaned = text[m.end():].lstrip("\n")
        return cleaned, group, arch, register
    # Потом toddler-формат (без «Группа», регистр опционален).
    m = STORY_MARKER_TODDLER_RE.match(text)
    if m:
        arch = int(m.group(1))
        register = int(m.group(2)) if m.group(2) else None
        cleaned = text[m.end():].lstrip("\n")
        return cleaned, None, arch, register
    return text, None, None, None


# Регулярка для названия сказки в кавычках-«ёлочках» в начале текста.
# Допускаем разные виды кавычек: «», „", “”, "", '...'.
# Название должно быть на отдельной строке (одной), потом перенос строки.
STORY_TITLE_RE = _re.compile(
    r"^[\s]*[«„“\"']\s*([^\n«»„“\"']{2,80}?)\s*[»„“\"']\s*\n+",
)


def parse_story_title(text: str) -> tuple[str, str | None]:
    """Извлекает название сказки из первой строки в «кавычках» и отрезает её.

    Ожидаемый формат: вторая строка ответа модели (после маркера архитектуры,
    который уже снят parse_story_marker) — это название в «ёлочках»:
        «Маша и хранитель осколков»

    Возвращает (cleaned_text, title). Если кавычек нет — (text, None).
    """
    m = STORY_TITLE_RE.match(text)
    if not m:
        return text, None
    title = m.group(1).strip()
    cleaned = text[m.end():].lstrip("\n")
    return cleaned, title


