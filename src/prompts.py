def pick_storyteller_prompt(child_age: int) -> tuple[str, str]:
    """Возвращает (system_prompt_template, rotation_hint_template) для возраста.

    Граница: ≤4 лет — toddler-промпт (8 архитектур, короче, проще),
            ≥5 лет — основной промпт (25 архитектур, сложнее, глубже).
    """
    if child_age <= 4:
        return SYSTEM_STORYTELLER_TODDLER, ROTATION_HINT_TODDLER_TEMPLATE
