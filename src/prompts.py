# ─────────────── Парсер названия сказки ───────────────
# Маркер архитектуры из старой схемы больше не парсится (бот сам знает
# параметры), а название сказки модель всё ещё пишет первой строкой —
# его извлекаем для PDF-заголовка и для отображения в библиотеке.
#
# Спека новая не требует «ёлочек» — допускаем обе формы: и в кавычках,
# и без. Берём первую непустую строку до первого \n как название, если
# она не выглядит как первое предложение сказки (короткая, без точки
# в конце, до 80 символов).

import re as _re

# Кавычки разные: «», „", "", '...'.
_QUOTED_TITLE_RE = _re.compile(
    r"^[\s]*[«„“\"']\s*([^\n«»„“”\"']{2,80}?)\s*[»„“”\"']\s*\n+",
)
# Без кавычек — короткая первая строка без точки в конце.
_BARE_TITLE_RE = _re.compile(
    r"^[\s]*([^\n]{2,80})\s*\n\s*\n",
)


def parse_story_title(text: str) -> tuple[str, str | None]:
    """Извлекает название из начала текста сказки и отрезает его.

    Сценарий 1 — название в кавычках-ёлочках на отдельной строке:
        «Маша и хранитель осколков»
        \n
        Текст сказки начинается...

    Сценарий 2 — название без кавычек, отдельной строкой, не похожее
    на первое предложение (короткое, без точки в конце):
        Маша и хранитель осколков
        \n
        Текст сказки начинается...

    Возвращает (cleaned_text, title). Если не удалось — (text, None).
    """
    if not text:
        return text, None
    m = _QUOTED_TITLE_RE.match(text)
    if m:
        title = m.group(1).strip()
        cleaned = text[m.end():].lstrip("\n")
        return cleaned, title
    m = _BARE_TITLE_RE.match(text)
    if m:
        candidate = m.group(1).strip()
        # Простая эвристика: если строка кончается точкой/восклицанием/
        # вопросом — это первое предложение сказки, не название.
        if candidate and candidate[-1] not in ".!?;…":
            cleaned = text[m.end():]
            return cleaned, candidate
    return text, None


# Парсер сцен из ответа сказочника. Сказочник по нашей инструкции в
# _SCENE_BLOCK_INSTRUCTIONS пишет сказку, потом разделитель
# ---SCENES--- и JSON под ним. Эта функция вырезает блок и возвращает
# (story_text_без_блока, scenes_dict_или_None).
import json as _json

_SCENES_SEPARATOR = "---SCENES---"


def parse_scenes_block(text: str) -> tuple[str, dict[str, str] | None]:
    """Вынимает блок ---SCENES--- из конца ответа сказочника.

    Сказочник по нашей инструкции пишет:
        [текст сказки]

        ---SCENES---
        {"opening": "...", "climax": "...", "ending": "..."}

    Возвращает (текст_без_блока, {opening, climax, ending} | None).
    Если блока нет / JSON битый / ключи не валидные → None и текст
    возвращается as-is (модель забыла блок — рисуем без мотива).

    Валидация ключей: каждое значение — строка длиной 8 < len < 100.
    Целевая длина по инструкции в _SCENE_BLOCK_INSTRUCTIONS — до 30
    char, но даём запас до 100 на случай если Claude перебрал. Хвост
    обрежется по бюджету Recraft (~28 char), но смысл сцены, скорее
    всего, в первых словах — обрезка по пробелу не разрушит.
    """
    if not text or _SCENES_SEPARATOR not in text:
        return text, None
    head, _, tail = text.rpartition(_SCENES_SEPARATOR)
    story_text = head.rstrip()
    # В tail может быть префиксный мусор (пробелы, переводы строк, ``` от
    # модели). Ищем первый { и последний } — это границы JSON.
    raw = tail.strip()
    if raw.startswith("```"):
        raw = raw.strip("`")
        if raw.lower().startswith("json"):
            raw = raw[4:].strip()
    open_idx = raw.find("{")
    close_idx = raw.rfind("}")
    if open_idx < 0 or close_idx <= open_idx:
        return story_text, None
    try:
        scenes = _json.loads(raw[open_idx : close_idx + 1])
    except Exception:
        return story_text, None
    if not isinstance(scenes, dict):
        return story_text, None
    for key in ("opening", "climax", "ending"):
        v = scenes.get(key)
        if not isinstance(v, str) or not (8 < len(v) < 200):
            return story_text, None
    return story_text, {
        "opening": scenes["opening"],
        "climax": scenes["climax"],
        "ending": scenes["ending"],
    }


