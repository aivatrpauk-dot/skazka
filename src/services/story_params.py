"""Выбор параметров сказки на стороне бота (не модели).

Это сердце «казино» — для каждой сказки бот сам выбирает 5 параметров
(форма, юмор, жанр, зачин, интонация) из словарей, исключая всё, что уже
было использовано в текущем цикле. Когда ось исчерпана — сброс, новый круг.

Так каждая ось проходит полный круг прежде чем повториться. Общая комби-
нация параметров повторится не раньше чем будет сделано столько сказок,
сколько в самом длинном словаре — практически не повторяется в реальной
жизни ребёнка.

Параметры идут в финал концептуального промпта как короткая надстройка:

    Сегодня:
    — форма: спираль
    — тип юмора: тёплый бытовой комизм
    — жанр: метафора
    — зачин: вопрос
    — интонация: загадочная

Модель видит эти 5 слов и сама знает, как их воплотить. Никаких описаний
архитектур в промпт не идёт — современные LLM знают что такое «спираль»
в детской сказке без наших пояснений.

Возрастная адаптация:
- 5-6 лет → полные словари (25 форм, 9 юморов, 7 зачинов, 7 интонаций,
  2 жанра с альтернацией)
- 3-4 года → сокращённые словари (8/5/5/5/1) — убираем то, что для
  трёхлетки слишком абстрактно (метафорические формы, ироничный юмор,
  меланхоличные интонации).
"""
