"""Выбор параметров сказки на стороне бота (не модели).

Это лёгкое «казино» — для каждой сказки бот выбирает 2 параметра
(форма, зачин) из словарей, исключая всё, что уже было использовано
в текущем цикле. Когда ось исчерпана — сброс, новый круг.

Раньше параметров было 5 (форма, юмор, жанр, зачин, интонация). В мае
2026 архитектура промптов переехала на 6 стилевых анкеров (см.
prompts.STORYTELLER_VARIANTS), и юмор/жанр/интонация стали частью
самого system-промпта — параметрами в user_message они только мешали,
конфликтуя со стилем анкера. Остались только форма и зачин — они дают
СТРУКТУРНУЮ вариативность сверху стиля, не конфликтуя с ним.

Возрастной сплит (3-4 vs 5-6) убран — продукт ориентирован на 5-7 лет
по новому позиционированию (премиум перед сном для дошкольников).

Параметры идут в финал user_message как короткая надстройка:

    Сегодня:
    — форма: спираль
    — зачин: вопрос

Модель видит эти 2 слова и сама знает, как их воплотить. Никаких
описаний архитектур в промпт не идёт — современные LLM знают что такое
«спираль» в детской сказке без наших пояснений.
"""
from __future__ import annotations

import logging
import random
from dataclasses import dataclass

logger = logging.getLogger(__name__)


# ─────────────────── СЛОВАРИ ───────────────────
# Хранятся как простые списки имён — без описаний. Описания в Part 5
# спеки (справочник для команды), это для людей, не для модели.

# Формы (сюжетные архитектуры).
FORMS = [
    "маршрут",
    "кумулятивная",
    "спираль",
    "параллель",
    "утрата и находка",
    "одна комната",
    "возвращение",
    "превращение наоборот",
    "гость из другого времени",
    "сказка без слов",
    "перевёртыш",
    "обмен",
    "сказка-загадка",
    "сказка-вопрос",
    "несуразица",
    "сказка-выбор без проигрыша",
    "сказка-починка",
    "спящий мир",
    "коллекционер",
    "хранитель",
    "молчаливый помощник",
    "маленький мир",
    "большой мир",
    "письмо",
    "сказка-сон во сне",
]

# Зачины — как начинается сказка. Эта ось дисциплинирует против
# «В тот вечер Маша...» в каждой второй сказке.
OPENINGS = [
    "вечерний тихий",      # описание времени суток, мягкий вход
    "прямое действие",     # «Маша села на подоконник и...»
    "вопрос",              # «Знаешь, что бывает, когда...»
    "диалог",              # сказка начинается с реплики
    "обстановка",          # описание места, без героя
    "присказка",           # «В одном городе, где...» — заходовый ритм
    "описание героя",      # портретный вход
]


# ─────────────────── РЕЗУЛЬТАТ ВЫБОРА ───────────────────

@dataclass
class StoryParams:
    """Готовые параметры для подстановки в финал промпта."""
    form: str
    opening: str
    # Обновлённые «used» массивы — handler сохраняет их в БД после
    # успешной генерации. Так мы не пишем в БД заранее (если LLM упал,
    # параметры считаются как-будто не использовались).
    new_used_architectures: list[int]   # int-индексы для совместимости с last_story_architecture
    new_used_openings: list[str]


# ─────────────────── ВЫБОР ИЗ ОДНОЙ ОСИ ───────────────────

def _pick_with_cycle(
    full_dict: list,
    used: list | None,
    axis_name: str,
) -> tuple[object, list]:
    """Возвращает (выбранное значение, обновлённый used).

    Логика «по кругу»:
    - Если used == [] → берём случайно из full_dict, used = [pick].
    - Если used содержит не все значения → берём случайно из (full_dict - used).
    - Если used == full_dict (цикл исчерпан) → СБРОС, новый круг.
      ВАЖНО: при сбросе мы исключаем ИЗ КАНДИДАТОВ значение, которое было
      выбрано последним в исчерпавшемся цикле, иначе оно может выпасть
      два раза подряд — раз в конце цикла, второй раз сразу после сброса.
      После выбора нового значения used = [chosen] (без last_of_previous_cycle).
    """
    if not full_dict:
        raise ValueError(f"Пустой словарь для оси {axis_name}")
    used = list(used or [])
    if len(full_dict) == 1:
        # Дегенеративный случай: словарь из одного элемента. Возвращаем
        # его и используем «used=[v]», цикл сразу исчерпан.
        return full_dict[0], [full_dict[0]]
    available = [v for v in full_dict if v not in used]
    if not available:
        # Цикл исчерпан — сброс. Новый круг.
        last_of_prev_cycle = used[-1] if used else None
        logger.info(
            "Ось «%s»: цикл из %d значений исчерпан, сброс (исключаем %r чтобы не повторить подряд)",
            axis_name, len(full_dict), last_of_prev_cycle,
        )
        # На новом круге исключаем только что использованное значение —
        # иначе оно может выпасть подряд два раза.
        used = []
        available = [v for v in full_dict if v != last_of_prev_cycle]
    chosen = random.choice(available)
    used.append(chosen)
    return chosen, used


# ─────────────────── ГЛАВНАЯ ФУНКЦИЯ ───────────────────

def pick_params(
    *,
    used_architectures: list[int] | None,
    used_openings: list[str] | None,
) -> StoryParams:
    """Выбирает форму и зачин для следующей сказки с учётом истории.

    Передаётся текущее состояние used-массивов из БД, возвращается
    выбор + обновлённые used-массивы (handler потом сохраняет их).

    used_architectures хранятся как int-индексы (1..N), used_openings —
    как строки. Это исторический разрыв: архитектуру раньше парсили из
    ответа модели по номеру, и legacy-колонка last_story_architecture
    (Integer) осталась числовой. Зачины новые → строки.
    """
    # Архитектура — выбираем по индексу 1..N, чтобы legacy
    # last_story_architecture (Integer) остался валидным.
    used_arch_idx = list(used_architectures or [])
    # Конвертим список индексов в список значений для проверки.
    used_arch_values = [FORMS[i - 1] for i in used_arch_idx if 1 <= i <= len(FORMS)]
    form, new_used_arch_values = _pick_with_cycle(FORMS, used_arch_values, "форма")
    # Из значений обратно в индексы.
    new_used_arch_idx = [FORMS.index(v) + 1 for v in new_used_arch_values]

    opening, new_used_openings = _pick_with_cycle(OPENINGS, used_openings, "зачин")

    return StoryParams(
        form=form,
        opening=opening,
        new_used_architectures=new_used_arch_idx,
        new_used_openings=new_used_openings,
    )
