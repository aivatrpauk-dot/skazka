# ─────────────────── РЕЗУЛЬТАТ ВЫБОРА ───────────────────

@dataclass
class StoryParams:
    """Готовые параметры для подстановки в финал промпта."""
    form: str
    humor: str
    genre: str
    opening: str
    tone: str
    # Обновлённые «used» массивы — handler сохраняет их в БД после
    # успешной генерации. Так мы не пишем в БД заранее (если LLM упал,
    # параметры считаются как-будто не использовались).
    new_used_architectures: list[int]   # int-индексы для совместимости с last_story_architecture
    new_used_humors: list[int]
    new_used_openings: list[str]
    new_used_tones: list[str]
    new_category: str                    # "MP" / "SW" / "TODDLER"


# ─────────────────── ВЫБОР ИЗ ОДНОЙ ОСИ ───────────────────

def _pick_with_cycle(
    full_dict: list,
    used: list | None,
    axis_name: str,
) -> tuple[object, list]:
    """Возвращает (выбранное значение, обновлённый used).

    Логика «по кругу»:
    - Если used == [] → берём случайно из full_dict, used = [pick].
    - Если used содержит не все значения → берём случайно из (full_dict - used).
    - Если used == full_dict (цикл исчерпан) → СБРОС, новый круг.
      ВАЖНО: при сбросе мы исключаем ИЗ КАНДИДАТОВ значение, которое было
      выбрано последним в исчерпавшемся цикле, иначе оно может выпасть
      два раза подряд — раз в конце цикла, второй раз сразу после сброса.
      После выбора нового значения used = [chosen] (без last_of_previous_cycle).
    """
    if not full_dict:
        raise ValueError(f"Пустой словарь для оси {axis_name}")
    used = list(used or [])
    if len(full_dict) == 1:
        # Дегенеративный случай: словарь из одного элемента. Возвращаем
        # его и используем «used=[v]», цикл сразу исчерпан.
        return full_dict[0], [full_dict[0]]
    available = [v for v in full_dict if v not in used]
    if not available:
        # Цикл исчерпан — сброс. Новый круг.
        last_of_prev_cycle = used[-1] if used else None
        logger.info(
            "Ось «%s»: цикл из %d значений исчерпан, сброс (исключаем %r чтобы не повторить подряд)",
            axis_name, len(full_dict), last_of_prev_cycle,
        )
        # На новом круге исключаем только что использованное значение —
        # иначе оно может выпасть подряд два раза.
        used = []
        available = [v for v in full_dict if v != last_of_prev_cycle]
    chosen = random.choice(available)
    used.append(chosen)
    return chosen, used


# ─────────────────── ВЫБОР ЖАНРА ───────────────────
# Жанр — особая ось: 2 значения, чередуем альтернацией (не через used-list).
# Это устойчивее: даже если кто-то сбросит used-массивы, чередование
# жанра останется ясным правилом.

def _pick_genre(child_age: int, last_category: str | None) -> tuple[str, str]:
    """Возвращает (genre_name, new_category_code).

    new_category_code: "MP" / "SW" / "TODDLER" — для совместимости со
    старой колонкой last_story_category (мы её не выпиливаем, она же
    используется в дашбордах и аналитике).
    """
    if child_age <= 4:
        return "простое волшебство", "TODDLER"
    # 5-6 лет: альтернация MP ↔ SW
    if last_category == "MP":
        return "простое волшебство", "SW"
    if last_category == "SW":
        return "метафора", "MP"
    # Первая сказка — случайно
    if random.random() < 0.5:
        return "простое волшебство", "SW"
    return "метафора", "MP"


# ─────────────────── ГЛАВНАЯ ФУНКЦИЯ ───────────────────

def pick_params(
    *,
    child_age: int,
    used_architectures: list[int] | None,
    used_humors: list[int] | None,
    used_openings: list[str] | None,
    used_tones: list[str] | None,
    last_category: str | None,
) -> StoryParams:
    """Выбирает 5 параметров для следующей сказки с учётом истории.

    Передаётся текущее состояние used-массивов из БД, возвращается
    выбор + обновлённые used-массивы (handler потом сохраняет их).

    used_architectures и used_humors хранятся как int-индексы (1..N),
    used_openings и used_tones — как строки. Это исторический разрыв:
    архитектуру и юмор раньше парсили из ответа модели по номеру, и
    легаси-колонки last_story_architecture / last_story_humor_register
    остались числовыми. Зачины и интонации новые → строки.
    """
    is_toddler = child_age <= 4
    forms_dict = FORMS_3_4 if is_toddler else FORMS_5_6
    humors_dict = HUMORS_3_4 if is_toddler else HUMORS_5_6
    openings_dict = OPENINGS_3_4 if is_toddler else OPENINGS_5_6
    tones_dict = TONES_3_4 if is_toddler else TONES_5_6

    # Архитектура и юмор — выбираем по индексу 1..N, чтобы legacy
    # last_story_architecture (Integer) остался валидным.
    used_arch_idx = list(used_architectures or [])
    # Конвертим список индексов в список значений для проверки.
    used_arch_values = [forms_dict[i - 1] for i in used_arch_idx if 1 <= i <= len(forms_dict)]
    form, new_used_arch_values = _pick_with_cycle(forms_dict, used_arch_values, "форма")
    # Из значений обратно в индексы.
    new_used_arch_idx = [forms_dict.index(v) + 1 for v in new_used_arch_values]

    used_hum_idx = list(used_humors or [])
    used_hum_values = [humors_dict[i - 1] for i in used_hum_idx if 1 <= i <= len(humors_dict)]
    humor, new_used_hum_values = _pick_with_cycle(humors_dict, used_hum_values, "тип юмора")
    new_used_hum_idx = [humors_dict.index(v) + 1 for v in new_used_hum_values]

    opening, new_used_openings = _pick_with_cycle(openings_dict, used_openings, "зачин")
    tone, new_used_tones = _pick_with_cycle(tones_dict, used_tones, "интонация")
    genre, new_category = _pick_genre(child_age, last_category)

    return StoryParams(
        form=form,
        humor=humor,
        genre=genre,
        opening=opening,
        tone=tone,
        new_used_architectures=new_used_arch_idx,
        new_used_humors=new_used_hum_idx,
        new_used_openings=new_used_openings,
        new_used_tones=new_used_tones,
        new_category=new_category,
    )
